    private void tableBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBukuMouseClicked
       DefaultTableModel model = (DefaultTableModel) tableBuku.getModel();
       kodeBuku.setText(model.getValueAt( tableBuku.getSelectedRow(),0).toString());
       judulBuku.setText(model.getValueAt(tableBuku.getSelectedRow(),0).toString());
       tglTerbit.setText(model.getValueAt(tableBuku.getSelectedRow(),0).toString());
       pengarang.setText(model.getValueAt(tableBuku.getSelectedRow(),0).toString());
       
    }//GEN-LAST:event_tableBukuMouseClicked

    private void deleteBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBukuMouseClicked
       DefaultTableModel model = (DefaultTableModel) tableBuku.getModel();
       if(tableBuku.getRowCount()==0){
           JOptionPane.showMessageDialog(null,"tabel kosong");
       }else if(tableBuku.getSelectedRow()==-1){
           JOptionPane.showMessageDialog(null, "pilih data akan dihapus : ");
       }
       else{
           int dialogButton = JOptionPane.YES_NO_OPTION;
           int dialogResult = JOptionPane.showConfirmDialog(null,"Anda yakin akan Menghapus");
           if(dialogResult == JOptionPane.YES_OPTION){
               model.removeRow(tableBuku.getSelectedRow());
           }
       }
               
    }//GEN-LAST:event_deleteBukuMouseClicked

    private void tableSiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSiswaMouseClicked
       DefaultTableModel model = (DefaultTableModel) tableSiswa.getModel();
       nim.setText(model.getValueAt( tableSiswa.getSelectedRow(),0).toString());
       nama.setText(model.getValueAt(tableSiswa.getSelectedRow(),0).toString());
       String jk = model.getValueAt(tableSiswa.getSelectedRow(),3).toString();
       if(jk.equalsIgnoreCase("Laki-Laki")){
           lakiLaki.setSelected(true);
           
       }
       perempuan.setSelected(true);
       
       String clas = model.getValueAt(tableSiswa.getSelectedRow(),4).toString();
       for(int i=0;i<kelas.getItemCount(); i++ ){
           if(clas.equalsIgnoreCase((String)kelas.getItemAt(i))){
           kelas.setSelectedIndex(i);
       }
       }
    }//GEN-LAST:event_tableSiswaMouseClicked

    private void inputSiswaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputSiswaActionPerformed
          if(nim.getText().trim().equals("")||
           nama.getText().trim().equals("")||
           (lakiLaki.isSelected() && perempuan.isSelected())){
           JOptionPane.showMessageDialog(null,"lengkapi data ");
        
        }
        else{
            String  kelamin = null;
            if(lakiLaki.isSelected()){
                kelamin="laki laki";
            }else if(perempuan.isSelected()){
                kelamin="perempuan";
            }
            DefaultTableModel model = (DefaultTableModel) tableSiswa.getModel();
            model.addRow(new Object[]{
               nim.getText(),
               nama.getText(),
               kelamin,
               kelas.getSelectedItem().toString()
            
            });
            JOptionPane.showMessageDialog(null,"data berhasil di simpan");
        }
                
    }//GEN-LAST:event_inputSiswaActionPerformed

    private void deleteSiswaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSiswaActionPerformed
       DefaultTableModel model = (DefaultTableModel) tableSiswa.getModel();
       if(tableSiswa.getRowCount()==0){
           JOptionPane.showMessageDialog(null,"tabel kosong");
       }else if(tableSiswa.getSelectedRow()==-1){
           JOptionPane.showMessageDialog(null, "pilih data akan dihapus : ");
       }
       else{
           int dialogButton = JOptionPane.YES_NO_OPTION;
           int dialogResult = JOptionPane.showConfirmDialog(null,"Anda yakin akan Menghapus");
           if(dialogResult == JOptionPane.YES_OPTION){
               model.removeRow(tableSiswa.getSelectedRow());
           }
       }
    }//GEN-LAST:event_deleteSiswaActionPerformed

    private void updateBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBukuActionPerformed
        if(tableBuku.getRowCount()==0){
            JOptionPane.showMessageDialog(null,"table kosong");
        }else if(tableBuku.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null,"pilih data yang ingin di update");
        }else{
            DefaultTableModel model = (DefaultTableModel) tableBuku.getModel();
            model.setValueAt(kodeBuku.getText(),tableBuku.getSelectedRow(),0);
            model.setValueAt(judulBuku.getText(), tableBuku.getSelectedRow(),0);
            model.setValueAt(tglTerbit.getText(), tableBuku.getSelectedRow(),0);
            model.setValueAt(pengarang.getText(), tableBuku.getSelectedRow(),0);
        }
        JOptionPane.showMessageDialog(null,"data berhasil di simpan");
    }//GEN-LAST:event_updateBukuActionPerformed

