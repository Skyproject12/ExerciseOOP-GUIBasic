    private void inputBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputBukuActionPerformed
       if(kodeBuku.getText().trim().equals("")||
          judulBuku.getText().trim().equals("")||
          tglTerbit.getText().trim().equals("")||
          pengarang.getText().trim().equals("")){
          JOptionPane.showMessageDialog(null,"lengkapi data ");
        
    }
        else{
            DefaultTableModel model = (DefaultTableModel) tableBuku.getModel();
            model.addRow(new Object[]{
               kodeBuku.getText(),
               judulBuku.getText(),
               tglTerbit.getText(),
               pengarang.getText(),
            
            });
            JOptionPane.showMessageDialog(null,"data berhasil di simpan");
            
        }
                
    }//GEN-LAST:event_inputBukuActionPerformed
   
